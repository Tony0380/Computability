const dictionaries = Object.fromEntries(
  languages.map((language, languageIndex) => [
    language.id,
    new Map(rows.map((row) => [row[0], row[languageIndex]])),
  ]),
) as Record<Language, Map<string, string>>;

export function translate(
  language: Language,
  source: string,
  values?: Record<string, string | number>,
): string {
  let result = dictionaries[language].get(source) ?? source;
  if (values)
    for (const [key, value] of Object.entries(values)) result = result.replaceAll(`{${key}}`, String(value));
  return result;
}

type I18nValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (source: string, values?: Record<string, string | number>) => string;
};
const I18nContext = createContext<I18nValue>({
  language: "it",
  setLanguage: () => undefined,
  t: (source) => source,
});

export function I18nProvider({
  language,
  setLanguage,
  children,
}: {
  language: Language;
  setLanguage: (language: Language) => void;
  children: ReactNode;
}) {
  return (
    <I18nContext.Provider
      value={{ language, setLanguage, t: (source, values) => translate(language, source, values) }}
    >
      {children}
    </I18nContext.Provider>
  );
}

export const useI18n = () => useContext(I18nContext);

export function detectedLanguage(): Language {
  const saved = localStorage.getItem("computability.language") as Language | null;
  if (saved && languages.some((language) => language.id === saved)) return saved;
  const browser = navigator.language.slice(0, 2).toLowerCase();
  return languages.some((language) => language.id === browser) ? (browser as Language) : "en";
}
